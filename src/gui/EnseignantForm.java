    private void bnAddEnsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnAddEnsActionPerformed
        String nom = txtNomEns.getText().toString();
        String prenom = txtPrenomEns.getText().toString();
        String matiere = txtMatiereEns.getText().toString();
        if(ens.create(new Enseignant(id,nom, prenom, matiere))){
            JOptionPane.showMessageDialog(this, "Bien enregistré");
            loadEnseignant();
        }else {
            JOptionPane.showConfirmDialog(this, "Problème");
        }
    }//GEN-LAST:event_bnAddEnsActionPerformed

    private void listeEnseignantsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listeEnseignantsMouseClicked
       id = Integer.parseInt(model.getValueAt(listeEnseignants.getSelectedRow(), 0).toString());
      txtNomEns.setText(model.getValueAt(listeEnseignants.getSelectedRow(), 1).toString());
      txtPrenomEns.setText(model.getValueAt(listeEnseignants.getSelectedRow(), 2).toString());
      txtMatiereEns.setText(model.getValueAt(listeEnseignants.getSelectedRow(), 3).toString());
    }//GEN-LAST:event_listeEnseignantsMouseClicked

    private void bnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnDeleteActionPerformed
       int response = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment supprimer cette filière ?");
     if(response == 0){
         ens.delete(ens.findById(id));
         loadEnseignant();
     }
    }//GEN-LAST:event_bnDeleteActionPerformed

    private void bnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnUpdateActionPerformed
        int response = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment modifier cette filière ?");
     if(response == 0){ 
        String nom = txtNomEns.getText().toString();
        String prenom = txtPrenomEns.getText().toString();
        String matiere = txtMatiereEns.getText().toString();
        if(ens.update(new Enseignant(id,nom,prenom,matiere))){
            JOptionPane.showMessageDialog(this, "Bien enregistrée");
            loadEnseignant();
        }else {
            JOptionPane.showConfirmDialog(this, "Problème");
        }
     }
    }//GEN-LAST:event_bnUpdateActionPerformed


