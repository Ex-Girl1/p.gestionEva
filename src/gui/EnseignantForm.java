    void loadEnseignant(){
        model.setRowCount(0);
        for(Enseignant es :ens.findAll()){
            model.addRow(new Object[]{
            es.getId(),
            es.getNom(),
            es.getPrenom(),
            es.getMatiere()}
          );          
        }
        }
            
    
